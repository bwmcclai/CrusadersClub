/** All cities as structured objects */
export const WORLD_CITIES: CityFull[] = RAW.map(([name, country, lat, lon, pop]) => ({
  name, country, lat, lon, pop,
}))

/** Filter to countries in the given ISO set, sorted by population desc */
export function getCitiesForCountries(isoIds: number[]): CityFull[] {
  const set = new Set(isoIds)
  return WORLD_CITIES
    .filter((c) => set.has(c.country))
    .sort((a, b) => b.pop - a.pop)
}
